// getCharWidth returns the width of a character in pixels
func getCharWidth(c byte) int {
	char, ok := font8x8[c]
	if !ok {
		return 4 // space width for unknown chars
	}
	// Find rightmost non-zero column
	width := 0
	for i := 7; i >= 0; i-- {
		if char[i] != 0 {
			width = i + 1
			break
		}
	}
	if width == 0 {
		width = 4 // minimum width for space
	}
	return width
}

// RenderTextToBitmap renders text to the Rio 500 bitmap format
// The Rio 500 uses 16 bytes per display column, 1536 bytes total (96 columns)
// Characters are 8 pixels tall, centered vertically in the 16-pixel display
// Returns the bitmap data and the number of characters rendered (used as num_blocks)
func RenderTextToBitmap(text string) ([]byte, int) {
	bitmap := make([]byte, 1536)

	// Character height is 8 pixels, display is 16 pixels tall
	// Skip = (16 - 8) / 2 = 4 bytes to center vertically
	skip := 4

	x := 0 // Current x position in pixels
	charCount := 0

	for _, r := range text {
		c := byte(r)
		if c > 127 {
			c = '?' // Replace non-ASCII with ?
		}

		charData, ok := font8x8[c]
		if !ok {
			charData = font8x8[' ']
		}

		charWidth := getCharWidth(c)

		// Check if we have room (max 96 columns * 8 pixels = 768 pixels, but we use columns)
		// Each column is 16 bytes, max 96 columns
		if x/8 >= 95 {
			break
		}

		// Calculate which 16-byte block we're in
		blockOffset := (x / 8) * 16
		bitOffset := x % 8

		// Write character data
		for col := 0; col < charWidth; col++ {
			if blockOffset >= 1536 {
				break
			}

			colData := charData[col]

			// Write to current block (with bit shifting if needed)
			for row := 0; row < 8; row++ {
				bit := (colData >> row) & 1
				if bit != 0 {
					bytePos := blockOffset + skip + row
					if bytePos < 1536 {
						bitmap[bytePos] |= (1 << (7 - bitOffset))
					}
				}
			}

			bitOffset++
			if bitOffset >= 8 {
				bitOffset = 0
				blockOffset += 16
			}
		}

		x += charWidth + 1 // character width plus 1 pixel spacing
		charCount++
	}

	// Return character count as num_blocks (this is how the original code works)
	if charCount == 0 {
		charCount = 1
	}

	return bitmap, charCount
}
